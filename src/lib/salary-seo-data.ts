/**
 * FAQ 생성 (기본 1문 + 구간별 1문)
 */
function generateFaq(
  amount: number,
  formatted: string,
  monthly: number,
  bracket: BracketKey,
  variant: number,
): { question: string; answer: string }[] {
  const grossMonthly = Math.round(amount / 12);
  const deduction = grossMonthly - monthly;

  // 기본 FAQ: 실수령액 질문
  const basicFaq = {
    question: `연봉 ${formatted} 실수령액은?`,
    answer: `부양가족 1명(본인) 기준 월 실수령액은 약 ${monthly}만원입니다. 세전 월 ${grossMonthly}만원에서 약 ${deduction}만원(4대보험+소득세)이 공제됩니다.`,
  };

  // 구간별 FAQ
  const bracketFaq = BRACKET_FAQ_TEMPLATES[bracket][variant](formatted, amount, monthly);

  return [basicFaq, bracketFaq];
}

