// 3-1. 월 실수령액 추정 (선형 보간)
// ============================================================

/**
 * 주어진 연봉(만원)에 대해 월 실수령액(만원)을 선형 보간으로 추정합니다.
 * 기준점: 2400→180, 3000→223, 4000→286, 5000→349, 6000→411,
 *         7000→471, 8000→524, 10000→641, 15000→886
 */
const TAKE_HOME_ANCHORS: [number, number][] = [
  [2400, 180],
  [3000, 223],
  [4000, 286],
  [5000, 349],
  [6000, 411],
  [7000, 471],
  [8000, 524],
  [10000, 641],
  [15000, 886],
];

function estimateMonthlyTakeHome(amount: number): number {
  // 하한/상한 클램핑
  if (amount <= TAKE_HOME_ANCHORS[0][0]) return TAKE_HOME_ANCHORS[0][1];
  if (amount >= TAKE_HOME_ANCHORS[TAKE_HOME_ANCHORS.length - 1][0])
    return TAKE_HOME_ANCHORS[TAKE_HOME_ANCHORS.length - 1][1];

  for (let i = 0; i < TAKE_HOME_ANCHORS.length - 1; i++) {
    const [x0, y0] = TAKE_HOME_ANCHORS[i];
    const [x1, y1] = TAKE_HOME_ANCHORS[i + 1];
    if (amount >= x0 && amount <= x1) {
      const ratio = (amount - x0) / (x1 - x0);
      return Math.round(y0 + ratio * (y1 - y0));
    }
  }
  return Math.round((amount / 12) * 0.78); // fallback
}

