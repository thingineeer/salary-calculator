/**
 * 연봉 금액(만원)에 대해 SEO 페이지 데이터를 자동 생성합니다.
 */
function generateSalaryPageData(amount: number): SalaryPageData {
  const bracketKey = getBracketKey(amount);
  const monthly = estimateMonthlyTakeHome(amount);
  const formatted = formatSalaryTitle(amount);
  const formattedNum = formatNumber(amount);

  // amount를 시드로 사용하여 다양한 변형 선택
  const variantIndex = ((amount - 2400) / 100) % 4;

  // 타이틀
  const title = `연봉 ${formatted} 실수령액`;

  // 설명
  const description = `2026년 기준 연봉 ${formatted}의 월 실수령액, 4대보험료, 소득세를 자동 계산합니다. 연봉 ${formattedNum}만원의 실제 월급을 확인하세요.`;

  // 대표 직업
  const jobs = BRACKET_JOBS[bracketKey][variantIndex];
  const representativeJobs = jobs.join(', ');

  // 생활 수준 메모
  const lifestyleNote = generateLifestyleNote(amount, monthly, bracketKey, variantIndex);

  // 세금 팁
  const taxTip = BRACKET_TAX_TIPS[bracketKey][variantIndex];

  // FAQ
  const faq = generateFaq(amount, formatted, monthly, bracketKey, variantIndex);

  return {
    amount,
    title,
    description,
    representativeJobs,
    lifestyleNote,
    taxTip,
    faq,
  };
}

