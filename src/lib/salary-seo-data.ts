/**
 * 연봉 금액에 따른 구간 분류
 */
type BracketKey =
  | 'entry'
  | 'smallMid'
  | 'large'
  | 'manager'
  | 'director'
  | 'executive';

function getBracketKey(amount: number): BracketKey {
  if (amount <= 3000) return 'entry';
  if (amount <= 4000) return 'smallMid';
  if (amount <= 5000) return 'large';
  if (amount <= 7000) return 'manager';
  if (amount <= 10000) return 'director';
  return 'executive';
}

