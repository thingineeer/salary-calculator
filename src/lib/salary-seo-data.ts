// ============================================================

/**
 * 연봉(만원)을 한국어 표기로 포맷합니다.
 * 예: 2400 → "2,400만원", 10000 → "1억원", 12500 → "1억 2,500만원"
 */
function formatSalaryTitle(amount: number): string {
  if (amount >= 10000) {
    const eok = Math.floor(amount / 10000);
    const remainder = amount % 10000;
    if (remainder === 0) {
      return `${eok}억원`;
    }
    return `${eok}억 ${remainder.toLocaleString('ko-KR')}만원`;
  }
  return `${amount.toLocaleString('ko-KR')}만원`;
}

/**
 * 숫자를 콤마 포맷합니다. (예: 2400 → "2,400")
 */
function formatNumber(n: number): string {
  return n.toLocaleString('ko-KR');
}

